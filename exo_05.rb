puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" # affichage de string

puts 3 + 2 < 5 - 7 # affichage d'un nombre

puts "Ça fait combien 3 + 2 ? #{3 + 2}" # affichage avec insertion de variable
puts "Ça fait combien 5 - 7 ? #{5 - 7}" # affichage avec insertion de variable

puts "Ok, c'est faux alors !" # affichage de string

puts "C'est drôle ça, faisons-en plus :" # affichage de string

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" # affichage avec insertion de variable
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" # affichage avec insertion de variable
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" # affichage avec insertion de variable

# que fait #{} cela permet d'inserer des variables dans des chaine de caracteres ou permet l'insertion d'un boolean ( ligne 20 - 22 )